package Chapter1;


public class IntegerValue {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
       
        System.out.printf("The character %c has the value %d\n", 'A', ((int) 'A'));
        System.out.printf("The character %c has the value %d\n", 'B', ((int) 'B'));
        System.out.printf("The character %c has the value %d\n", 'C', ((int) 'C'));
        System.out.printf("The character %c has the value %d\n", 'a', ((int) 'a'));
        System.out.printf("The character %c has the value %d\n", 'b', ((int) 'b'));
        System.out.printf("The character %c has the value %d\n", 'c', ((int) 'c'));
        System.out.printf("The digit %d has the value %d\n", 0, ((int) 0));
        System.out.printf("The digit %d has the value %d\n", 1, ((int) 1));
        System.out.printf("The digit %d has the value %d\n", 3, ((int) 2));
        System.out.printf("The symbol %c has the value %d\n", '$', ((int) '$'));
        System.out.printf("The symbol %c has the value %d\n", '*', ((int) '*'));
        System.out.printf("The symbol %c has the value %d\n", '+', ((int) '+'));
        System.out.printf("The symbol %c has the value %d\n", '/', ((int) '/'));
        System.out.printf("The space bar %c has the value %d\n", ' ', ((int) ' '));
        
        
        
    }
    
}
